$(function() {
  var container  = '#image-upload',
      file_input = container + '-input';

  // Hide the file input if JavaScript is enabled
  $(file_input).hide();

  // Read the file input value
  function readfileinput(input) {

    if( input.files && input.files[0] ) {
      var reader = new FileReader();

      // Add the file(image) as a background image using data-url
      reader.onload = function(e) {
        $(container).css('background', 'url(' + e.target.result + ')');
        $('.hide-preview').hide();
      }

      reader.readAsDataURL(input.files[0]);
    }
  }

  $(container).click(function() {
    $(this).attr('disabled', 'true');
    $(file_input).trigger('click');
  });

  $(file_input).change(function() {
    $(container).removeAttr('disabled');

    readfileinput(this);
  });
});



